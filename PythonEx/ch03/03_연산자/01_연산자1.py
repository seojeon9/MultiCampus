# 2. 지수 **
# 3. 곱셈 * , 나눗셈 /, //, 나머지 %
# 4. 덧셈 +, 뺄셈 -
# 5. 대입 연산자

# 대입연산자와 산술연산자
# 특정변수에 특정값을 산술연산을 통해 변경한 후 변경된 값을 다시 해당변수에 저장시키는 경우

a = 0
a = a + 1
a += 1

