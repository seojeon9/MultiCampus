# 산술 연산자 정리
print(5 + 4) # 정수연산 : 9
print(5.0 - 3.0) # 실수연산 : 2.0
print(1.5 * 4) # 실수연산 : 6.0
print(200 / 10) # 실수연산 : 20.0
print(20 / 7) # 실수 연산 : 2.8571....
print(20 // 7) # 나눗셈(몫) : 2
print(20 % 7) # 나눗셈(나머지) : 6

# 아래 변수를 이용해 몇 분 몇 초인지 출력
seconds = 1000
minutes = seconds // 60 # 몫
remainder = 1000 % 60 # 나머지
print("%d분 %d초 " % (minutes, remainder))

