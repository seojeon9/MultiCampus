#  정수 : int
num = 100
print("num : ", type(num)) #num :  <class 'int'>

#  실수 : float
PI = 3.14
print("PI : ", type(PI)) #PI :  <class 'float'>

#  문자열 : str
name = "홍길동"
print("name :", type(name)) #name : <class 'str'>

price = '1000'
print("price : ", type(price))

done = True
print("done : ", type(done)) #done :  <class 'bool'>